def product(*numbers):
    a=1
    if not isinstance(numbers,int and float and list and tuple ):
        raise TypeError('请输入一个或多个整数或小数')
    elif len(numbers)<0:
        return '请输入一个或多个数'
    for n in numbers:
        a=a*n
    return a
# 测试
print('product(5) =', product(5))
